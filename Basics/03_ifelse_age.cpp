#include<bits/stdc++.h>
using namespace std;
/*Write a program to check whether a person is an adult or not. 
If the age of the person is >=18, then he/she is an adult otherwise not.
*/
int main(){
    int age;
    cin>>age;
    if(age>=18){
        cout<<"You are an adult"<<endl;
    }
    else{
        cout<<"You are not an adult"<<endl;
    }
    
    return 0;
    
}